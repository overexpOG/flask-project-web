const validateSelect = (Select) => {
    if (!Select) return false;

    return Select.value != "";
}

const validateSelectM = (Select) => {
    //calcular el largo
    let selectedOptions = Array.from(Select.selectedOptions);

    return selectedOptions.length;
}

const validateFiles = (Files) => {
    if (!Files) return false;

    //validacion de la cantidad correcta de imagenes
    let lengthValid = 1 <= Files.length && Files.length <= 3;

    //valido que sean imagenes
    let typeValid = true;
    for (const x of Files) {
        let fileFamaly = x.type.split("/")[0];
        typeValid &&= fileFamaly == "image";
    }

    return lengthValid && typeValid;
}

const validateName = (Name) => {
    if (!Name) return false;

    //validación del largo (entre 3 y 80 palabras)
    let lengthValid = Name.trim().length >= 3 && Name.length <= 80;    

    //validación del formato (se aceptan solo letras y espacios)
    let re = /^[A-Za-z\s\'\u00E1\u00E9\u00ED\u00F3\u00FA]+$/;
    let formatValid = re.test(Name);

    return lengthValid && formatValid;
}

const validateEmail = (Email) => {
    if (!Email) return false;

    //validación del formato
    let re = /^[a-zA-Z0-9._%+-]+@[a-zA-Z_]+?\.[a-zA-Z.]{2,}$/;
    let formatValid = re.test(Email);

    return formatValid; 
}

const validatePhone = (Phone) => {
    if (!Phone) return true;

    //validación del largo (se va a suponer el +56 de chile, por lo que hay que ingresar 9 números más)
    let lengthValid = (Phone.length == 9 || Phone.length == 0);

    //validación del formato
    let re = /^[0-9]+$/;
    let formatValid = re.test(Phone);

    return lengthValid && formatValid;
}

const validateForm = () => {
    //variables y funciones auxiliares
    let invalidInputs = [];
    let isValid = true;
    const setInvalidInput = (InputName) => {
        invalidInputs.push(InputName);
        isValid &&= false;
    }

    const NTipoArtesano = validateSelectM(TipoArtesano);

    //valido los distintos parametros del form
    if (!validateSelect(RegionArtesano)) {
        setInvalidInput("No se ah seleccionado una Region");
    }
    if (!validateSelect(ComunaArtesano)) {
        setInvalidInput("No se ah seleccionado una Comuna");
    }
    if (NTipoArtesano == 0) {
        setInvalidInput("No se ah seleccionado un tipo de artesanía");
    }
    if (NTipoArtesano > 3) {
        setInvalidInput("Solo se pueden seleccionar 3 artesanias");
    }
    if ((ImagenArtesano == null) || !validateFiles(ImagenArtesano.files)) {
        setInvalidInput("cantidad invalida de fotos");
    }
    if (!validateName(NameArtesano.value)) {
        setInvalidInput("Nombre invalido");
    }
    if (!validateEmail(EmailArtesano.value)) {
        setInvalidInput("Email invalido");
    }
    if (!validatePhone(PhoneArtesano.value)) {
        setInvalidInput("Numero de celular invalido");
    }
    let validationBox = document.getElementById("val-box-artesano");
    let validationMessageElem = document.getElementById("val-msg-artesano");
    let validationListElem = document.getElementById("val-list-artesano");

    //limpio el contenido anterior
    validationListElem.textContent = "";
    validationMessageElem.innerText = "";

    if (!isValid) {
        //añado los elementos incorrectos al mensaje de error
        for (input of invalidInputs) {
            let listElement = document.createElement("li");
            listElement.innerText = input;
            validationListElem.append(listElement);
        }
        //mensaje de error
        validationMessageElem.innerText = "Los siguientes elementos son invalidos";
        //hago el mensaje de error visible
        validationBox.hidden = false;
    } else {
        confirmationArtesano.style.display = "block";
    }
}

